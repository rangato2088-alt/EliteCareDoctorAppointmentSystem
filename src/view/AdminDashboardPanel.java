    private void loadDoctorsTable() {

    javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel)
            tblDoctors.getModel();

    model.setRowCount(0);

    DoctorDAO dao = new DoctorDAO();

    for (Doctor doctor : dao.getAllDoctors()) {

        model.addRow(new Object[]{
            doctor.getDoctorId(),
            doctor.getDoctorName(),
            doctor.getSpecialization(),
            doctor.getPhone(),
            doctor.getStatus(),
            doctor.getConsultationFee()
        });
    }
}
    
    private void loadStatistics() {

    UserDAO userDAO =
            new UserDAO();

    DoctorDAO doctorDAO =
            new DoctorDAO();

    lblUsers.setText(
            String.valueOf(
                    userDAO.getTotalUsers()));

    lblActiveUsers.setText(
            String.valueOf(
                    userDAO.getActiveUsers()));

    lblDoctors.setText(
            String.valueOf(
                    doctorDAO.getTotalDoctors()));

    lblActiveDoctors.setText(
            String.valueOf(
                    doctorDAO.getActiveDoctors()));
}
    

